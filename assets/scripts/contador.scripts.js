function soma (){
    let numeroA = Number(document.getElementById("numeroA").value);
    let numeroB = Number(document.getElementById("numeroB").value);

    let result = numeroA + numeroB;

    document.getElementById("result").innerText = result;
}

function sub (){
    let numeroA = Number(document.getElementById("numeroA").value);
    let numeroB = Number(document.getElementById("numeroB").value);


    let result = numeroA - numeroB;

    document.getElementById("result").innerText = result;
}

